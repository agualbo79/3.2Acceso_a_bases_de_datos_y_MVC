class Customer:
    def __init__(self, customer_id, first_name, last_name, email, phone, street, city, state, zip_code):
        self.customer_id = customer_id
        self.first_name = first_name
        self.last_name = last_name
        self.email = email
        self.phone = phone
        self.street = street
        self.city = city
        self.state = state
        self.zip_code = zip_code
